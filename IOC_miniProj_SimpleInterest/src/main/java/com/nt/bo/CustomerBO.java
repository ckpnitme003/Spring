package com.nt.bo;

public class CustomerBO {
	
	private String cname;
	private String caddrs;
	private Double pamt;
	private Double intramt;
	
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCaddrs() {
		return caddrs;
	}
	public void setCaddrs(String caddrs) {
		this.caddrs = caddrs;
	}
	public Double getPamt() {
		return pamt;
	}
	public void setPamt(Double pamt) {
		this.pamt = pamt;
	}
	public Double getIntramt() {
		return intramt;
	}
	public void setIntramt(Double intramt) {
		this.intramt = intramt;
	}
	
	
	
}
